# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def isBalanced(self, root: TreeNode) -> bool:

        def judge(root):
            if not root or self.flag == False : return 0
            left = judge(root.left)
            right = judge(root.right)
            if abs(left - right) > 1 : self.flag = False
            return max(left ,right) + 1


        self.flag = True
        judge(root)
        return self.flag
